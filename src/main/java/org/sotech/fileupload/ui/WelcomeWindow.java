    private void ClickPause(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClickPause
        if (ApplicationConfig.status) {
            ApplicationConfig.status = Boolean.FALSE;
            txt_user_home.setText("");
            txt_user_connect.setText("");
            btn_send_file.setEnabled(false);
            btn_upload.setEnabled(false);
            label_status.setText("No Conectado");
            btn_conectar.setEnabled(true);
            btn_ver_archivo.setEnabled(false);
            ApplicationConfig.me = null;
            ApplicationConfig.userConnect = null;
            StartManager.windows.setContadorReceiveReset();
            StartManager.windows.setContadorSendReset();
            int rows = ApplicationConfig.model_receive.getRowCount();
            if (rows > 0) {
                for (int i = rows - 1; i >= 0; i--) {
                    ApplicationConfig.model_receive.removeRow(i);
                }
            }
            rows = ApplicationConfig.model_sending.getRowCount();
            if (rows > 0) {
                for (int i = rows - 1; i >= 0; i--) {
                    ApplicationConfig.model_sending.removeRow(i);
                }
            }
            rows = ApplicationConfig.model_editing.getRowCount();
            if (rows > 0) {
                for (int i = rows - 1; i >= 0; i--) {
                    ApplicationConfig.model_editing.removeRow(i);
                }
            }
            ClientManager.close();
        }
    }//GEN-LAST:event_ClickPause

    private void btn_send_fileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_send_fileActionPerformed

        try {
            btn_upload.setEnabled(false);
            users = DocumentoService.listar_usuarios_conectados(ApplicationConfig.me.getId());
            if (!users.isEmpty()) {
                String[] choices = new String[users.size()];
                for (int i = 0; i < users.size(); i++) {
                    Usuario obj = users.get(i);
                    choices[i] = obj.getSearch();
                }
                String input = (String) JOptionPane.showInputDialog(null, "Seleccione uno ..",
                        "Usuarios Disponibles", JOptionPane.QUESTION_MESSAGE, null,
                        choices,
                        choices[1]);
                if (input != null) {
                    txt_user_connect.setText(input);
                    ApplicationConfig.userConnect = ApplicationConfig.users.get(input);
                    //ClientManager.mkdirToUser(ApplicationConfig.userConnect.getId(), ApplicationConfig.me.getId());
                    btn_upload.setEnabled(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No hay usuarios conectados");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_send_fileActionPerformed

